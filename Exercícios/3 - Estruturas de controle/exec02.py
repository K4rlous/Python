# Loops

"For Loop: Usado para iterar sobre uma sequência (lista, tupla, string, etc.)."
# Iterar sobre uma lista
frutas = ["maçã", "banana", "cereja", "manga"]
for fruta in frutas:
    print(fruta)

"While Loop: Repete enquanto uma condição for verdadeira."
# Loop enquanto x for menor que 5
x = 0
while x < 5:
    print(x)
    x += 1