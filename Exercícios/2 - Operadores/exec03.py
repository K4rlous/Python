# Operadores de Atribuição 

a, b, c, d, e, f = 3, 4, 5, 6, 7, 8

# Operador = equivale a: x = 1
a = 7

# Operador += equivale a: x = x + 1
b += 3

# Operador -= equivale a: x = x - 1
c -= 2

# Operador *= equivale a: x = x * 1
d *= 4

# Operador /= equivale a: x = x / 1
e /= 4

# Operador %= equivale a: x = x % 1
f %= 2

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)