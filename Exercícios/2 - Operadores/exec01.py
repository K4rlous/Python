# Operadores Aritméticos

n1 = int(input("Digite o 1° número: "))
n2 = int(input("Digite o 2° número: "))

# Adição	Realiza a soma de ambos operandos. +
adicao = f"A soma dos números é {n1+n2}"

# Subtração	Realiza a subtração de ambos operandos. -
subtracao = f"A subtracao dos números é {n1-n2}"

# Multiplicação	Realiza a multiplicação de ambos operandos. *
multiplicacao = f"A multiplicação dos números é {n1*n2}"

# Divisão	Realiza a Divisão de ambos operandos. /
divisao = f"A divisão dos números é {n1/n2}"

# Divisão inteira	Realiza a divisão entre operandos e a parte decimal de ambos operandos. //
divisaoint = f"A divisão inteira dos números é {n1//n2}"

# Módulo	Retorna o resto da divisão de ambos operandos. %
modulo = f"O modulo dos números é {n1%n2}"

# Exponenciação	Retorna o resultado da elevação da potência pelo outro. **
exponenciacao = f"A exponenciação dos números é {n1**n2}"


print(adicao)
print(subtracao)
print(multiplicacao)
print(divisao)
print(divisaoint)
print(modulo)
print(exponenciacao)