# Indentação e Comentarios

if 10 > 5:
    print("Dez é maior que cinco")  # Este bloco está indentado
    if 10 > 7:
        print("E também maior que sete")  # Este bloco está mais um nível indentado

"Python usa a indentação para agrupar declarações, ele não aceitará código sem a indentação correta; isso resulta em erros de sintaxe, use a mesma quantidade de espaços (geralmente 4) para cada nível de indentação, qualquer declaração que introduza um novo bloco de código (como funções, loops, condicionais) deve terminar com : e a linha seguinte deve ser indentada, cada subnível adicional de bloco deve ser indentado mais um nível, isso facilita a manutenção do código e o deixa mais legível e organizado"

